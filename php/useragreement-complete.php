            <a name="classification"></a>
            <h4> Information Classification Explained</h4>
            <p> Institutional Data is defined as a data or information element that satisfies one
                or more of the following criteria:</p>
            <ul>
                <li>it is relevant to planning, managing, operating, or auditing a major administrative
                    function of the university</li>
                <li>it is referenced or required for use by more than one organizational unit</li>
                <li>it is included in an official university administrative report; or</li>
                <li>it is used to derive an element that meets the criteria above</li>
            </ul>
            <p> Data Stewards apply the following criteria to classify institutional data and information
                into four levels:</p>
            <ul>
                <li>Public</li>
                <li>University-internal</li>
                <li>Restricted</li>
                <li>Critical</li>
            </ul>
            <p> <strong>Public data</strong> is defined as data to which little to no restrictions
                apply. The general public may be granted access to such data.</p>
            <p> <strong>University-internal data</strong> is defined as data that may be accessed
                by all eligible employees of the university, without restriction, in the conduct
                of university business. This should be the "default" classification for all data.</p>
            <p> <strong>Restricted data</strong> may not be accessed without specific authorization,
                or only selective access may be granted because of legal, ethical, or other constraints.</p>
            <p> <strong>Critical data</strong> is defined as data by which inappropriate handling
                could result in criminal or civil penalties, identity theft, personal financial
                loss, invasion of privacy, and/or unauthorized access to this type of information
                by an individual or many individuals.</p>
