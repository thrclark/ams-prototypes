            <a name="intro"></a>
            <h4> Introduction</h4>
            <p> You have been directed to this Acceptable Use Agreement for Access to Technology
                and Information Resources as a new or continuing faculty, staff, hourly, or affiliated
                employee at Indiana University. </p>
            <p> Access to Indiana University data and information, and access to IT accounts, systems,
                and applications, is based on your need for access and your assent to use that access
                appropriately. These services are integral to the operation of the university, and
                security and privacy laws and other institutional policies protect much of the information.</p>
            <p> Therefore, before you can be granted access, you must read and agree to follow these
                acceptable usage standards, and must accept responsibility to preserve the security
                and confidentiality of information that you access, in any form, including oral,
                print, or electronic formats. </p>
            <p> Read the information below carefully. It begins with an explanation of how data
                use is governed within IU, and then sets out user responsibilities. Although these
                general provisions apply to all IU information and IT accounts, systems, and applications,
                please be aware that managers of certain services or information types may require
                you to complete additional agreements and/or training. </p>
