            <a name="gov"></a>
            <h4> Information Governance Explained</h4>
            <p> The university has assigned the following roles concerning data and information.</p>
            <p> <strong>Ownership:</strong> Indiana University is the owner of the university's
                institutional data and information.</p>
            <p> <strong>Data Stewards:</strong> These are senior university officials who have planning
                and policy-level stewardship responsibility for specific segments of the university's
                information resources (usually within functional areas). Data Stewards establish
                standard rules, guidelines, and profiles for information access, and also make recommendations
                and decisions about individual requests to access information. The responsibility
                for such recommendations may be delegated to a Data Manager.</p>
            <p> <strong>Data Managers:</strong> These are university officials and their employees
                who have operational-level responsibility for information management activities
                related to the access, capture, maintenance, and dissemination of data and information.</p>
            <a href="#top" class="pull-right">↑ top</a>
